#include <iostream>
using namespace std;

int main() {
    int count = 0; // Variable to keep track of iterations

    // Example loop
    for (int i = 0; i < 10; i++) {
        count++; // Increment count for each iteration
        cout << "Iteration: " << i << endl;
    }

    cout << "Total iterations: " << count << endl;
    return 0;
}




